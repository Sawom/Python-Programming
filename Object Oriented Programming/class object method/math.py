import math

num1 = int(input("enter 1st number : "))
num2 = int(input("enter 2nd number : "))

print("PI = ",math.pi)
print("Power = ",math.pow(num1,num2))
print("square root of num1 =  ",math.sqrt(num1))
print("Floor of an example = ",math.floor(5.325))
print("Ceil of an example = ",math.ceil(5.325))
print("gcd of num1 and num2 = ",math.gcd(num1,num2))
print("lcm of num1 and num2 = ",math.lcm(num1,num2,45))
